export const EMAIL_REGEX = /^[^\s@]+@[^\s@]+\.[^\s@]{2,}$/i;
export const ZIP_REGEX = /^\d{5}(-\d{4})?$/;
export const NAME_REGEX = /^[a-zA-Z\s\-']+$/;

export function formatUSPhone(raw: string): string {
  const cleaned = raw.replace(/\D/g, '');
  let digits = cleaned;
  if (digits.length === 11 && digits.startsWith('1')) {
    digits = digits.substring(1);
  }
  if (digits.length === 0) return '';
  if (digits.length <= 3) return `(${digits}`;
  if (digits.length <= 6) return `(${digits.slice(0, 3)}) ${digits.slice(3)}`;
  return `(${digits.slice(0, 3)}) ${digits.slice(3, 6)}-${digits.slice(6, 10)}`;
}

export function validateUSPhone(raw: string): boolean {
  const cleaned = raw.replace(/\D/g, '');
  if (cleaned.length === 10) return true;
  if (cleaned.length === 11 && cleaned.startsWith('1')) return true;
  return false;
}

export function formatZipCode(raw: string): string {
  const cleaned = raw.replace(/[^\d-]/g, '');
  if (cleaned.length > 5 && !cleaned.includes('-')) {
    return `${cleaned.slice(0, 5)}-${cleaned.slice(5, 9)}`;
  }
  return cleaned.slice(0, 10);
}
